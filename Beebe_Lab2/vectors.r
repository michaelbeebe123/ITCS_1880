# ---------------------------------------
#          INT VECTORS x AND y
# ---------------------------------------
x <- c(1:10)
y <- c(-2:7)

# ---------------------------------------
#       MATH OPERATIONS ON VECTOR x
# ---------------------------------------
x[4] / x[2]
x[5] * x[3]

# ---------------------------------------
#       MATH OPERATIONS ON VECTOR y
# ---------------------------------------
y[1] * x[5]
y[2] + x[10]

# ---------------------------------------
#      LENGTH OF x, y AND x + y
# ---------------------------------------
length(x)
length(y)
length(x) + length(y)

# ---------------------------------------
#         CHARACTER VECTOR f
# ---------------------------------------
f <- c("Horror", "Comedy", "Drama", "Science Fiction", "Thriller")

# ---------------------------------------
#     CONVERT VECTOR f TO A FACTOR
# ---------------------------------------
f2 <- as.factor(f)
f2

# ---------------------------------------
#       ISSUE as.numeric TO f2
# ---------------------------------------
as.numeric(f2)
